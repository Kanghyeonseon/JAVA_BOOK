package Ch03실수;

public class C03자료형 {

	public static void main(String[] args) {
		
		int num1; 				//4byte 정수 저장공간을 지정한 뒤 num1으로 이름을 부여(변수를 정의)
								//선언과 정의는 차이가 있다.
		num1 = 10;			    //10이라는 데이터가 상수풀에 저장된 후 num1 변수에 복사된다.
		int num2 = 4;			//4라는 이터를 상수풀에 저장한 후 4byte정수 num2 공간에 초기화
		int num3 = num1 + num2; //num1과 num2의 합을 num3변수공간에 초기화한다.
		System.out.println(num3);//함수 내부에서 변수가 사용되면 '값'으로해석된다.
		
				
		
		
		
		
		
	}

}
